# Exemplo com a built-in function 'print'
print()
print("Hello World!")
print("Aprendendo", "Python")

# Exemplo com a built-in function 'type'
print(type("Hello, World!"))  # str
print(type(120))  # int
print(type(120.01))  # float

# Exemplo de variáveis
x = "Hello, World!"
print(x)

# Exemplo de operações
print(5 - 59)
print(11 / 3)
print(12 / 2)
print(11 % 3)  # resto da divisão de 11 por 3
print(11 // 3)  # quociente da divisão de 11 por 3
print(3**2)  # 3 elevado a 2
print("aw" + "op")  # concatenação de strings
